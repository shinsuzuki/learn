#!/bin/bash
# --------------------------------------------------------------------------------
# gemini.learn.sh
# --------------------------------------------------------------------------------

# 堅牢化のオプション設定
# set -e (errexit): コマンドが失敗した時点でスクリプトを即座に終了させる。 << エラーチェックの前で終了するためあまり使えない
# set -u (nounset): 未定義の変数を使おうとした時にエラーを出す（タイポ防止）。
# set -o pipefail: パイプラインの途中でエラーが起きても、全体の終了ステータスを「失敗」として扱う。
#set -euo pipefail
set -uo pipefail

# メイン処理開始
echo "処理を開始します..."

echo $0
echo $(basename "data/list.txt")


# cat <<EOF > conifg.conf
# PORT=8080
# EOF


# スクリプトの冒頭で exec 2> error.log と書くと、
# それ以降のすべてのコマンドのエラー出力が自動的にそのファイルへ送られます。
# 個別に 2> を書く手間が省けるため、大規模なスクリプトで重宝します。

LOG_ERROR="error.log"
ls -al /tmp_xxx 2>> "$LOG_ERROR"

cat<<EOF
----------
check  errorlog: "${LOG_ERROR}"
----------
EOF

# プロのテクニック：${var##*/} と ${var#*/}
#  - # が1つなら「最短一致」、## なら「最長一致」で削除します。
#  - パスからファイル名を取り出すときは、一番右側の / までをごっそり消したいので ##*/ を使うのが定番です。

# 前方一致削除 ${var##*/}: 最初の / までを消す。
# 後方一致削除 ${var%/*}: 最後の / 以降（ファイル名部分）を消して「ディレクトリ名」を抽出。
# 拡張子の除去 ${var%.*}: 最後の . 以降を消す。

fullpath="/usr/data/test.txt"
echo ${fullpath##*/}  # 最初の / までを消す。
echo ${fullpath%/*}   # 最後の / 以降（ファイル名部分）を消して「ディレクトリ名」を抽出。
echo ${fullpath%.*}   # 最後の . 以降を消す。

# 拡張子を置換
echo ${fullpath} | sed 's/.txt$/.bak/'





# メイン処理完了
echo "処理が正常に完了しました。"
